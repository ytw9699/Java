package d_연산자;

public class b_나머지 {

	    public static void main(String[] args) {
	        int a = 3;
	        System.out.println(0%a);//	0/3의 나머지는 0
	        System.out.println(1%a);//	1/3의 나머지는 1
	        System.out.println(2%a);//	2/3의 나머지는 2
	        System.out.println(3%a);//  0
	        System.out.println(4%a);//  1
	        System.out.println(5%a);//  2
	        System.out.println(6%a);//  0
	    }
}
