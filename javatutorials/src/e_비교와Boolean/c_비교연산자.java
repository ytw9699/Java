package e_비교와Boolean;

public class c_비교연산자 {

	public static void main(String[] args) {
		 System.out.println(10 > 20);       //false
	        System.out.println(10 > 2);            //true
	        System.out.println(10 > 10);           //false
	        System.out.println(10 >= 20); // false
	        System.out.println(10 >= 2); // true
	        System.out.println(10 >= 10); // true
	        
	        
	}

}
